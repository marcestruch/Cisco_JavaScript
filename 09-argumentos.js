function suma(a){
  return a+2;
}

let resultado = suma(5);
console.log(resultado); // 7
// En este caso, la función suma toma un argumento a y le suma 2.

function suma1(a, b){
  return a+b;
}

let resultado1 = suma1(5, 3);
console.log(resultado1); // 8
// En este caso, la función suma1 toma dos argumentos a y b y devuelve su suma.